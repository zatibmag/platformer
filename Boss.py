class Ogre_Leader():
    def __init__(self, x, y, width, height):

        self.x = x
        self.y = y
        self.width = width
        self.height = height
        self.vel = 5
        self.StandingCount = 0
        self.UpCount = 0
        self.walkingCount = 0
        self.returnCount = 0
        self.hit_cd = 0
        self.cd_head = 200
        self.deathCount = 0
        self.Ogre_head = 0
        self.Up = True
        self.Standing = False
        self.returnRight = False
        self.returnLeft = False
        self.walkRight = False
        self.walkLeft = False
        self.Block = True
        self.hit = False
        self.cd_head_block_Right = False
        self.cd_head_block_Left = False
        self.death = False
        self.block = False
        self.move_false = False
        self.Health_point = 10
        self.manX = 0
        self.manY = 0
        self.ManHitboxAttackLeft1 = False
        self.ManHitboxAttackRight1 = False

    def draw(self, win):
        if self.move_false == True:
            self.walkRight = False
            self.walkRight = False
            self.returnLeft = False
            self.returnRight = False

        win.blit(rock, (1300, 680))

        if self.UpCount == 40:
            self.Up = False

        if self.cd_head < 200:
            self.cd_head += 1

        if self.deathCount + 1 == 48:
            self.block = True  # добавить фулл скрин победи вместо фолс

        if self.returnRight == True and self.death == False:
            self.returnCount += 1
            if self.returnCount + 1 == 12:
                self.returnRight = False
                self.returnCount = 0
                self.walkRight = True
            win.blit(Ogre_return_right[self.returnCount // 4], (self.x, self.y))

        if self.returnLeft == True and self.death == False:
            self.returnCount += 1
            if self.returnCount + 1 == 12:
                self.returnCount = 0
                self.returnLeft = False
                self.walkLeft = True
            win.blit(Ogre_return_left[self.returnCount // 4], (self.x, self.y))

        if self.walkRight == True and self.death == False and self.move_false == False:
            if self.walkingCount == 32:
                self.walkingCount = 0
            win.blit(Ogre_walking_right[self.walkingCount // 4], (self.x, self.y - 20))
            self.x += self.vel
            self.walkingCount += 1
            if self.x > self.manX:
                self.walkingCount = 0
                self.walkRight = False
                self.returnLeft = True

        if self.walkLeft == True and self.death == False and self.move_false == False:
            if self.walkingCount == 32:
                self.walkingCount = 0
            win.blit(Ogre_walking_left[self.walkingCount // 4], (self.x, self.y - 20))
            self.x -= self.vel
            self.walkingCount += 1
            if self.x < self.manX:
                self.walkingCount = 0
                self.walkLeft = False
                self.returnRight = True


        if self.Up == True and self.manX <= 599:
            win.blit(Ogre_Leader_sit[0], (self.x, self.y))

        if self.manX >= 600 and self.Up == True:
            win.blit(Ogre_Leader_sit[self.UpCount // 10], (self.x, self.y))
            self.UpCount += 1

        if self.UpCount == 40:
            self.Standing = True
            if self.Standing == True:                                    #добавить сюда потом диалог
                win.blit(Ogre_Leader_Standing[self.StandingCount // 15], (1140, 540))
                self.StandingCount += 1
                if self.StandingCount + 1 == 46:
                    self.Standing = False
                    self.UpCount = 0

        if self.Up == False and self.Standing == False and self.Block:
            self.Block = False
            self.walkLeft = True

        if self.Up == False:
            win.blit(hp_boss[self.Health_point // 1], (50, -200))

        if self.ManHitboxAttackRight1 and self.manX + 100 > self.x and self.manX - 10 < self.x and self.hit == False:
            self.Health_point -= 1
            self.hit = True

        elif self.ManHitboxAttackLeft1 and self.manX - 150 < self.x and self.manX > self.x and self.hit == False:
            self.Health_point -= 1
            self.hit = True

        if self.Health_point // 1 <= 5 and self.manX + 50 < self.x and self.cd_head == 200 and self.cd_head_block_Right == False or self.cd_head_block_Left == True:
            self.move_false = True
            self.Ogre_head += 1
            self.cd_head_block_Left = True
            win.blit(Ogre_Leader_head_attack_Left[self.Ogre_head // 4], (self.x, self.y))
            self.x -= self.vel * 3
            if self.Ogre_head + 1 == 32:
                self.Ogre_head = 0
            if self.x <= 5:
                self.cd_head_block_Left = False
                self.move_false = False
                self.walkLeft = True
                self.cd_head = 0

        if self.Health_point // 1 <= 5 and self.manX - 50 > self.x and self.cd_head == 200 and self.cd_head_block_Left == False or self.cd_head_block_Right == True:
            self.move_false = True
            self.Ogre_head += 1
            self.cd_head_block_Right = True
            win.blit(Ogre_Leader_head_attack_Right[self.Ogre_head // 4], (self.x, self.y))
            self.x += self.vel * 3
            if self.Ogre_head + 1 == 32:
                self.Ogre_head = 0
            if self.x >= 1300:
                self.cd_head_block_Right = False
                self.move_false = False
                self.walkRight = True
                self.cd_head = 0


        if self.hit:
            self.hit_cd += 1
            if self.hit_cd == 20:
                self.hit_cd = 0
                self.hit = False
        if self.Health_point // 1 == 0:
            self.death = True

        if self.death == True and self.block == False and self.manX < self.x:
            self.deathCount += 1
            win.blit(Ogre_death_Left[self.deathCount // 8], (self.x, self.y))
        elif self.death == True and self.block == False and self.manX > self.x:
            self.deathCount += 1
            win.blit(Ogre_death_Right[self.deathCount // 8], (self.x, self.y))

